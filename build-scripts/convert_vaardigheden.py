"""
Convert 'uitleg vaardigheden.docx' to interactive HTML with sidebar navigation.
Usage: python convert_vaardigheden.py [paragraph_folder_path]
       python convert_vaardigheden.py --all   (processes all paragraphs)

HOW TO ADAPT
============
This script reads the standard vaardigheden Word template (as generated by
econ-word-templates Template A) and produces a self-contained HTML page with:
- Sticky sidebar with section navigation + domain badges
- IntersectionObserver active-state tracking
- Hero header with preview cards
- Per-section: begrippen tables, formula boxes, voorbeeld boxes, callouts
- Interactive checklist
- Responsive mobile menu

Domain color mapping can be extended in DOMAIN_MAP below.
"""
import sys, io, os, glob, html as html_mod
sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding='utf-8')
from docx import Document

