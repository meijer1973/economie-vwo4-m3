def find_paragraph_info(folder_path):
    """Extract paragraph number and name from folder path."""
    # Folder name like "3.2.4 Paragraaf 4 - Oligopolie"
    basename = os.path.basename(folder_path.rstrip('/\\'))
    # Extract number (e.g., "3.2.4")
    parts = basename.split(' ', 1)
    number = parts[0]
    # Extract name after " - "
    if ' - ' in basename:
        name = basename.split(' - ', 1)[1]
    else:
        name = parts[1] if len(parts) > 1 else basename
    return number, name


def process_paragraph(para_folder):
    """Process a single paragraph folder."""
    para_number, para_name = find_paragraph_info(para_folder)

    # Find the vaardigheden docx
    pattern = os.path.join(para_folder, '2. Leren', '*vaardigheden*.docx')
    files = glob.glob(pattern)
    if not files:
        print(f'  SKIP {para_number}: no vaardigheden.docx found')
        return False

    docx_path = files[0]

    try:
        data = build_sections_from_doc(docx_path)
    except Exception as e:
        print(f'  ERROR {para_number}: {e}')
        return False

    if not data['sections']:
        print(f'  SKIP {para_number}: no sections found')
        return False

    html_content = generate_html(data, para_number, para_name)

    # Write HTML file
    html_filename = f'{para_number} {para_name} \u2013 uitleg vaardigheden.html'
    html_path = os.path.join(para_folder, '2. Leren', html_filename)

    with open(html_path, 'w', encoding='utf-8') as f:
        f.write(html_content)

    print(f'  OK {para_number} {para_name} ({len(data["sections"])} sections, {len(data["checklist"])} checklist items)')
    return True


def main():
    if len(sys.argv) < 2:
        print('Usage: python convert_voorkennis.py [path] or --all')
        sys.exit(1)

    if sys.argv[1] == '--all':
        # Find all paragraph folders
        base = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
        folders = sorted(glob.glob(os.path.join(base, '3.*/3.*.*')))

        print(f'Found {len(folders)} paragraph folders')
        success = 0
        for folder in folders:
            if os.path.isdir(folder):
                if process_paragraph(folder):
                    success += 1
        print(f'\nDone: {success}/{len(folders)} converted')
    else:
        process_paragraph(sys.argv[1])


if __name__ == '__main__':
    main()
